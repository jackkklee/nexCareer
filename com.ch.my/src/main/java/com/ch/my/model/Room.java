package com.ch.my.model;

public class Room {
	private int room_num;
	private String room_subject;
	private int room_people;
	private int people_count;
	private String room_mem_id;
	public int getRoom_num() {
		return room_num;
	}
	public void setRoom_num(int room_num) {
		this.room_num = room_num;
	}
	public String getRoom_subject() {
		return room_subject;
	}
	public void setRoom_subject(String room_subject) {
		this.room_subject = room_subject;
	}
	public int getRoom_people() {
		return room_people;
	}
	public void setRoom_people(int room_people) {
		this.room_people = room_people;
	}
	public int getPeople_count() {
		return people_count;
	}
	public void setPeople_count(int people_count) {
		this.people_count = people_count;
	}
	public String getRoom_mem_id() {
		return room_mem_id;
	}
	public void setRoom_mem_id(String room_mem_id) {
		this.room_mem_id = room_mem_id;
	}

}
